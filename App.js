const App = () => {
  const [LoggedIn, setLoggedIn] = useState(false)

  const isLoggedin = async() =>{
    const result = await AsyncStorage.getItem("userToken")
    if(result){
      setLoggedIn(true)
    }else{
      setLoggedIn(false)
    }
  }

  useEffect(() => {
    isLoggedin()
  }, [])

  return (
    <>
      {LoggedIn?
      <View>
        <Text>asdasd</Text>
      </View>:
      <Login/>}
    </>
  );
};

